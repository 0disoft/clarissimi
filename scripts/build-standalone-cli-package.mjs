import { copyFile, mkdir, readFile, rm, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

import { build } from "esbuild";

import { addNoticeAfterShebang } from "./bundle-action.mjs";

const defaultRepoRoot = dirname(dirname(fileURLToPath(import.meta.url)));

export const standaloneCliPackageContract = {
  name: "clarissimi",
  version: "0.1.1",
  bin: { clarissimi: "dist/clarissimi.js" },
  files: ["dist", "README.md", "LICENSE"],
  license: "Apache-2.0",
  engines: { node: ">=24" },
  publishConfig: { access: "public" },
};

export const standaloneCliPackagePaths = {
  sourceDir: join("distribution", "npm", "clarissimi"),
  outputDir: join(".tmp", "npm", "clarissimi"),
  entryPoint: join("packages", "cli", "dist", "bin", "clarissimi.js"),
  bundledCli: join("dist", "clarissimi.js"),
};

export async function buildStandaloneCliPackage(options = {}) {
  const repoRoot = options.repoRoot ?? defaultRepoRoot;
  const sourceDir = join(repoRoot, standaloneCliPackagePaths.sourceDir);
  const outputDir = join(repoRoot, standaloneCliPackagePaths.outputDir);
  const manifestPath = join(sourceDir, "package.json");
  const manifest = JSON.parse(await readFile(manifestPath, "utf8"));
  const manifestIssues = validateStandaloneCliPackageManifest(manifest);
  if (manifestIssues.length > 0) {
    throw new Error(
      `Standalone CLI package manifest is invalid:\n- ${manifestIssues.join("\n- ")}`,
    );
  }

  const result = await build({
    absWorkingDir: repoRoot,
    bundle: true,
    entryPoints: [join(repoRoot, standaloneCliPackagePaths.entryPoint)],
    format: "esm",
    legalComments: "inline",
    logLevel: "silent",
    minify: false,
    platform: "node",
    target: "node24",
    treeShaking: true,
    write: false,
  });
  const outputFile = result.outputFiles?.[0];
  if (outputFile === undefined) {
    throw new Error("Standalone CLI bundling did not produce an output file.");
  }

  const generatedNotice = [
    "// Clarissimi standalone CLI npm package.",
    "// Generated by `pnpm run bundle:cli-package`; do not edit by hand.",
    "",
  ].join("\n");
  const bundledText = addNoticeAfterShebang(outputFile.text, generatedNotice);

  await rm(outputDir, { force: true, recursive: true });
  await mkdir(join(outputDir, "dist"), { recursive: true });
  await copyFile(manifestPath, join(outputDir, "package.json"));
  await copyFile(join(sourceDir, "README.md"), join(outputDir, "README.md"));
  await copyFile(join(repoRoot, "LICENSE"), join(outputDir, "LICENSE"));
  await writeFile(join(outputDir, standaloneCliPackagePaths.bundledCli), bundledText, "utf8");

  console.log(`Standalone CLI package staged at ${standaloneCliPackagePaths.outputDir}`);
  return { manifest, outputDir };
}

export function validateStandaloneCliPackageManifest(manifest) {
  const issues = [];
  for (const [key, expected] of Object.entries(standaloneCliPackageContract)) {
    if (JSON.stringify(manifest[key]) !== JSON.stringify(expected)) {
      issues.push(`${key} must equal ${JSON.stringify(expected)}.`);
    }
  }

  if (manifest.private === true) {
    issues.push("private must not be true for the standalone publication manifest.");
  }
  for (const field of [
    "dependencies",
    "devDependencies",
    "optionalDependencies",
    "peerDependencies",
  ]) {
    if (manifest[field] !== undefined) {
      issues.push(`${field} must be omitted from the dependency-free standalone package.`);
    }
  }
  if (manifest.scripts !== undefined) {
    issues.push("scripts must be omitted so installation cannot run lifecycle code.");
  }

  return issues;
}

if (process.argv[1] !== undefined && pathToFileURL(process.argv[1]).href === import.meta.url) {
  await buildStandaloneCliPackage();
}
