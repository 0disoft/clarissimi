import { readFile, mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

import { build } from "esbuild";

const defaultRepoRoot = dirname(dirname(fileURLToPath(import.meta.url)));

export async function runBundleAction(options = {}) {
  const repoRoot = options.repoRoot ?? defaultRepoRoot;
  const entryPoint = join(
    repoRoot,
    "packages",
    "action",
    "dist",
    "bin",
    "clarissimi-action.js",
  );
  const outputPath = join(repoRoot, "action-dist", "index.js");
  const checkOnly = options.checkOnly ?? false;

  const result = await build({
    absWorkingDir: repoRoot,
    bundle: true,
    entryPoints: [entryPoint],
    format: "esm",
    legalComments: "inline",
    logLevel: "silent",
    minify: false,
    platform: "node",
    target: "node24",
    treeShaking: true,
    write: false,
  });

  const outputFile = result.outputFiles?.[0];
  if (outputFile === undefined) {
    throw new Error("Action bundling did not produce an output file.");
  }

  const generatedNotice = [
    "// Clarissimi bundled GitHub Action.",
    "// Generated by `pnpm run bundle:action`; do not edit by hand.",
    "",
  ].join("\n");
  const bundledText = addNoticeAfterShebang(outputFile.text, generatedNotice);

  if (checkOnly) {
    let trackedText;
    try {
      trackedText = await readFile(outputPath, "utf8");
    } catch (error) {
      throw new Error(
        `Tracked Action bundle is missing or unreadable: ${error.message}`,
      );
    }

    if (trackedText !== bundledText) {
      throw new Error(
        "Tracked Action bundle is stale. Run `pnpm run bundle:action` and commit action-dist/index.js.",
      );
    }

    console.log("Action bundle check passed");
    return;
  }

  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, bundledText, "utf8");
  console.log("Action bundle written to action-dist/index.js");
}

export function addNoticeAfterShebang(text, notice) {
  if (!text.startsWith("#!")) {
    return `${notice}${text}`;
  }

  const newlineIndex = text.indexOf("\n");
  if (newlineIndex === -1) {
    return `${text}\n${notice}`;
  }

  return `${text.slice(0, newlineIndex + 1)}${notice}${text.slice(newlineIndex + 1)}`;
}

if (
  process.argv[1] !== undefined &&
  pathToFileURL(process.argv[1]).href === import.meta.url
) {
  await runBundleAction({
    checkOnly: process.argv.slice(2).includes("--check"),
  });
}
